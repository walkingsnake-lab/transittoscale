import { mkdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const NATURAL_EARTH_PRIMARY_URL =
  'https://raw.githubusercontent.com/nvkelso/natural-earth-vector/master/geojson/ne_50m_admin_0_countries.geojson';
const NATURAL_EARTH_FALLBACK_URL =
  'https://raw.githubusercontent.com/nvkelso/natural-earth-vector/master/geojson/ne_110m_admin_0_countries.geojson';
const LOCATOR_SPECS = [
  { region: 'United States', adminName: 'United States of America', pickTop: 1 },
  { region: 'Canada', adminName: 'Canada', pickTop: 1 },
  { region: 'Mexico', adminName: 'Mexico', pickTop: 1 },
  { region: 'Spain', adminName: 'Spain', pickTop: 1 },
  { region: 'Sweden', adminName: 'Sweden', pickTop: 1 },
  { region: 'United Kingdom', adminName: 'United Kingdom', pickTop: 2 },
  { region: 'China', adminName: 'China', pickTop: 1 }
];
const VIEWBOX_WIDTH = 100;
const MIN_VIEWBOX_HEIGHT = 56;
const MAX_VIEWBOX_HEIGHT = 112;
const SIMPLIFY_TOLERANCE = 0.14;
const COORDINATE_DECIMALS = 2;

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, '..');
const cacheDir = path.join(repoRoot, 'tmp-debug');
const sourcePath = path.join(cacheDir, 'ne_50m_admin_0_countries.geojson');
const outputPath = path.join(repoRoot, 'src', 'lib', 'country-locators.js');

await mkdir(cacheDir, { recursive: true });
const { geojson, sourceUrl } = await loadSourceGeojson(sourcePath);
const countryLocators = Object.fromEntries(
  LOCATOR_SPECS.map((spec) => {
    const feature = geojson.features.find((candidate) => candidate?.properties?.ADMIN === spec.adminName);

    if (!feature) {
      throw new Error(`Country not found in source data: ${spec.adminName}`);
    }

    const polygons = extractOuterRings(feature.geometry);
    const selectedPolygons = pickLargestPolygons(polygons, spec.pickTop);
    const bounds = computeBounds(selectedPolygons);
    const lonScale = resolveLongitudeScale(bounds);
    const projectedBounds = computeProjectedBounds(selectedPolygons, lonScale);
    const viewBoxHeight = resolveViewBoxHeight(bounds, projectedBounds);
    const outlinePath = selectedPolygons
      .map((ring) =>
        buildSvgPathForRing(ring, {
          bounds,
          projectedBounds,
          lonScale,
          viewBoxWidth: VIEWBOX_WIDTH,
          viewBoxHeight
        })
      )
      .filter(Boolean)
      .join(' ');

    return [
      spec.region,
      {
        viewBoxWidth: VIEWBOX_WIDTH,
        viewBoxHeight,
        lonScale: roundTo(lonScale, 5),
        bounds: {
          minLon: roundTo(bounds.minLon, 4),
          maxLon: roundTo(bounds.maxLon, 4),
          minLat: roundTo(bounds.minLat, 4),
          maxLat: roundTo(bounds.maxLat, 4)
        },
        outlinePath
      }
    ];
  })
);

const output = `// Generated by scripts/build-country-locators.mjs\n// Source: Natural Earth (public domain) ${sourceUrl}\n\nexport const COUNTRY_LOCATOR_BY_REGION = ${JSON.stringify(
  countryLocators,
  null,
  2
)};\n`;

await writeFile(outputPath, output);

console.log(`Wrote ${outputPath}`);

async function loadSourceGeojson(filePath) {
  try {
    const content = await readFile(filePath, 'utf8');
    return {
      geojson: JSON.parse(content),
      sourceUrl: NATURAL_EARTH_PRIMARY_URL
    };
  } catch (error) {
    if (error.code !== 'ENOENT') {
      throw error;
    }
  }

  const sourceCandidates = [NATURAL_EARTH_PRIMARY_URL, NATURAL_EARTH_FALLBACK_URL];
  let lastError = null;

  for (const candidateUrl of sourceCandidates) {
    try {
      const response = await fetch(candidateUrl);

      if (!response.ok) {
        throw new Error(`Unable to download Natural Earth source (${response.status}).`);
      }

      const body = await response.text();
      await writeFile(filePath, body);

      return {
        geojson: JSON.parse(body),
        sourceUrl: candidateUrl
      };
    } catch (error) {
      lastError = error;
    }
  }

  throw lastError ?? new Error('Unable to load Natural Earth source data.');
}

function extractOuterRings(geometry) {
  if (!geometry || !geometry.coordinates) {
    return [];
  }

  if (geometry.type === 'Polygon') {
    return geometry.coordinates.length > 0 ? [geometry.coordinates[0]] : [];
  }

  if (geometry.type === 'MultiPolygon') {
    return geometry.coordinates
      .map((polygon) => polygon[0])
      .filter((ring) => Array.isArray(ring) && ring.length > 2);
  }

  return [];
}

function pickLargestPolygons(polygons, pickTop) {
  return [...polygons]
    .sort((left, right) => Math.abs(computeRingArea(right)) - Math.abs(computeRingArea(left)))
    .slice(0, Math.max(1, pickTop));
}

function computeRingArea(ring) {
  if (!Array.isArray(ring) || ring.length < 3) {
    return 0;
  }

  let area = 0;

  for (let index = 0; index < ring.length; index += 1) {
    const [x1, y1] = ring[index];
    const [x2, y2] = ring[(index + 1) % ring.length];
    area += x1 * y2 - x2 * y1;
  }

  return area / 2;
}

function computeBounds(polygons) {
  let minLon = Infinity;
  let maxLon = -Infinity;
  let minLat = Infinity;
  let maxLat = -Infinity;

  for (const ring of polygons) {
    for (const [lon, lat] of ring) {
      minLon = Math.min(minLon, lon);
      maxLon = Math.max(maxLon, lon);
      minLat = Math.min(minLat, lat);
      maxLat = Math.max(maxLat, lat);
    }
  }

  if (!Number.isFinite(minLon) || !Number.isFinite(maxLon) || !Number.isFinite(minLat) || !Number.isFinite(maxLat)) {
    throw new Error('Unable to compute bounds for country geometry.');
  }

  return { minLon, maxLon, minLat, maxLat };
}

function computeProjectedBounds(polygons, lonScale) {
  let minX = Infinity;
  let maxX = -Infinity;

  for (const ring of polygons) {
    for (const [lon] of ring) {
      const x = projectLongitude(lon, lonScale);
      minX = Math.min(minX, x);
      maxX = Math.max(maxX, x);
    }
  }

  if (!Number.isFinite(minX) || !Number.isFinite(maxX)) {
    throw new Error('Unable to compute projected bounds for country geometry.');
  }

  return { minX, maxX };
}

function resolveLongitudeScale(bounds) {
  const centerLat = (bounds.minLat + bounds.maxLat) / 2;
  return clamp(Math.cos((centerLat * Math.PI) / 180), 0.35, 1);
}

function resolveViewBoxHeight(bounds, projectedBounds) {
  const lonRange = Math.max(projectedBounds.maxX - projectedBounds.minX, 1e-6);
  const latRange = Math.max(bounds.maxLat - bounds.minLat, 1e-6);
  const ratio = latRange / lonRange;
  const preferredHeight = VIEWBOX_WIDTH * ratio;
  return clamp(roundTo(preferredHeight, 1), MIN_VIEWBOX_HEIGHT, MAX_VIEWBOX_HEIGHT);
}

function buildSvgPathForRing(ring, { bounds, projectedBounds, lonScale, viewBoxWidth, viewBoxHeight }) {
  const projectedWidth = Math.max(projectedBounds.maxX - projectedBounds.minX, 1e-6);
  const latHeight = Math.max(bounds.maxLat - bounds.minLat, 1e-6);
  const projected = ring
    .map(([lon, lat]) => {
      const x = ((projectLongitude(lon, lonScale) - projectedBounds.minX) / projectedWidth) * viewBoxWidth;
      const y = ((bounds.maxLat - lat) / latHeight) * viewBoxHeight;
      return [x, y];
    })
    .filter(([x, y]) => Number.isFinite(x) && Number.isFinite(y));
  const simplifiedRing = simplifyClosedRing(projected, SIMPLIFY_TOLERANCE);

  if (simplifiedRing.length < 4) {
    return '';
  }

  const [firstPoint, ...restPoints] = simplifiedRing;
  const commands = [`M${formatNumber(firstPoint[0])} ${formatNumber(firstPoint[1])}`];

  for (const point of restPoints.slice(0, -1)) {
    commands.push(`L${formatNumber(point[0])} ${formatNumber(point[1])}`);
  }

  commands.push('Z');
  return commands.join(' ');
}

function simplifyClosedRing(points, tolerance) {
  const deduped = dedupeSequentialPoints(points);

  if (deduped.length < 3) {
    return deduped;
  }

  const openPoints = deduped.slice();
  const first = openPoints[0];
  const last = openPoints[openPoints.length - 1];

  if (pointsEqual(first, last)) {
    openPoints.pop();
  }

  const simplifiedOpen = simplifyLine(openPoints, tolerance);
  const safeSimplifiedOpen = simplifiedOpen.length >= 3 ? simplifiedOpen : openPoints;
  const closedRing = [...safeSimplifiedOpen, safeSimplifiedOpen[0]];

  return closedRing.map(([x, y]) => [roundTo(x, COORDINATE_DECIMALS), roundTo(y, COORDINATE_DECIMALS)]);
}

function simplifyLine(points, tolerance) {
  if (points.length <= 2) {
    return points;
  }

  const firstPoint = points[0];
  const lastPoint = points[points.length - 1];
  let maxDistance = -1;
  let pivotIndex = 0;

  for (let index = 1; index < points.length - 1; index += 1) {
    const distance = perpendicularDistance(points[index], firstPoint, lastPoint);

    if (distance > maxDistance) {
      maxDistance = distance;
      pivotIndex = index;
    }
  }

  if (maxDistance > tolerance) {
    const left = simplifyLine(points.slice(0, pivotIndex + 1), tolerance);
    const right = simplifyLine(points.slice(pivotIndex), tolerance);
    return [...left.slice(0, -1), ...right];
  }

  return [firstPoint, lastPoint];
}

function perpendicularDistance(point, lineStart, lineEnd) {
  const [x, y] = point;
  const [x1, y1] = lineStart;
  const [x2, y2] = lineEnd;
  const deltaX = x2 - x1;
  const deltaY = y2 - y1;

  if (!deltaX && !deltaY) {
    return Math.hypot(x - x1, y - y1);
  }

  const projection = ((x - x1) * deltaX + (y - y1) * deltaY) / (deltaX ** 2 + deltaY ** 2);
  const clampedProjection = clamp(projection, 0, 1);
  const projectionX = x1 + clampedProjection * deltaX;
  const projectionY = y1 + clampedProjection * deltaY;

  return Math.hypot(x - projectionX, y - projectionY);
}

function dedupeSequentialPoints(points) {
  const deduped = [];

  for (const point of points) {
    if (deduped.length === 0 || !pointsEqual(deduped[deduped.length - 1], point)) {
      deduped.push(point);
    }
  }

  return deduped;
}

function pointsEqual(left, right) {
  if (!left || !right) {
    return false;
  }

  return left[0] === right[0] && left[1] === right[1];
}

function formatNumber(value) {
  return Number.parseFloat(value.toFixed(COORDINATE_DECIMALS)).toString();
}

function roundTo(value, decimals) {
  const multiplier = 10 ** decimals;
  return Math.round(value * multiplier) / multiplier;
}

function clamp(value, min, max) {
  return Math.min(max, Math.max(min, value));
}

function projectLongitude(longitude, lonScale) {
  return longitude * lonScale;
}
